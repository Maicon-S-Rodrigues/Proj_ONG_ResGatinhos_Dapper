CREATE DATABASE Resgatinhos;
/*-------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
USE Resgatinhos;
/*-------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
/*-------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
CREATE TABLE Pessoa 
	(
		CPF VARCHAR (11) NOT NULL,
		Nome VARCHAR (50) NOT NULL,
		Sexo CHAR (1) NOT NULL,
		Data_Nascimento DATE NOT NULL,
		Telefone VARCHAR (12) NULL,
		Estado VARCHAR (30) NOT NULL,
		Cidade VARCHAR (50) NOT NULL,
		Bairro VARCHAR (50) NOT NULL,
		Rua VARCHAR (50) NOT NULL,
		Numero INT NOT NULL,
		Complemento VARCHAR (50) DEFAULT 'N/REGISTRADO',

		CONSTRAINT PK_CPF_Pessoa PRIMARY KEY (CPF)
	);
/*-------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
/*-------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
CREATE TABLE Animal
	(
		CHIP INT IDENTITY,
		Familia VARCHAR (35) NOT NULL DEFAULT 'N/REGISTRADO',
		Raca VARCHAR (50) DEFAULT 'N/REGISTRADO',
		Nome VARCHAR (50) DEFAULT 'N/REGISTRADO',
		Sexo CHAR (1) NOT NULL,
		Situacao VARCHAR (20) DEFAULT 'DISPONIVEL',

		CONSTRAINT PK_CHIP_Animal PRIMARY KEY (CHIP)
	);
/*-------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
/*-------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
CREATE TABLE Adota
	(
	CPF VARCHAR (11) NOT NULL,
	CHIP INT NOT NULL,
	
	FOREIGN KEY (CPF) REFERENCES Pessoa (CPF),
	FOREIGN KEY (CHIP) REFERENCES ANIMAL (CHIP),
	CONSTRAINT PK_ADOTA PRIMARY KEY (CHIP)
	);
